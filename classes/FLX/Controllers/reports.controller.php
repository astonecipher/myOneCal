namespace FLX\Controllers;

class reports
{
    
    private $view;
    
    public function __construct()
    {
        $this->vars['body'] = "<div class='well'>Reports View</div>";
    }
    
    public function data()
    {
        return $this->vars;
    }

    public function view()
    {
        if ($_GET["mobile"] == "yes") {
            return "mobile";
        } else {
            return $this->view;
        }
    }

    public function transfer()
    {
        return $this->transfer;
    }
}